    public void getData(){
        DefaultTableModel tabel1= new DefaultTableModel();
        tabel1.addColumn("Nama");
        tabel1.addColumn("Alamaat");
        tabel1.addColumn("Jabatan");
        tabel1.addColumn("hp");
        try {
           
           Connection conn = Koneksi.koneksiDB();
           Statement stm = conn.createStatement();
           ResultSet sql = stm.executeQuery("select * from pengurus");
           while(sql.next()){
                tabel1.addRow(new Object[]{
                    sql.getString(2),
                    sql.getString(3),
                    sql.getString(4),
                    sql.getString(5)
                });
            }
            tabel.setModel(tabel1);
        }catch (SQLException e) {
        }
    }
   
