private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Date ys=new Date(); // membuat oject ys dari class Date 
            SimpleDateFormat s=new SimpleDateFormat("yyyy-MM-dd"); //membuat object
           Map<String, Object> parameters = new HashMap<String, Object>();
           JasperPrint print = JasperFillManager.fillReport("./Laporan/Peng.jasper", parameters, Koneksi.koneksiDB());
           JRExporter exporter = new JRPdfExporter();
           exporter.setParameter(JRExporterParameter.JASPER_PRINT, print);
           exporter.setParameter(JRExporterParameter.OUTPUT_STREAM, new FileOutputStream("./lap/Pengurus/Laporan Semua Pengurus("+s.format(ys)+").pdf")); // your output goes here
           exporter.exportReport();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Pengurus.class.getName()).log(Level.SEVERE, null, ex);
        } catch (JRException ex) {
            Logger.getLogger(Pengurus.class.getName()).log(Level.SEVERE, null, ex);
        }
     
}//GEN-LAST:event_jButton1ActionPerformed

